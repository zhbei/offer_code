#include <iostream>
#include <vector>
#include <cstdio>
#include <queue>

using namespace std;

struct BinaryTreeNode{
	int val;
	BinaryTreeNode* left;
	BinaryTreeNode* right;
};
void helper(BinaryTreeNode* node,vector<vector<BinaryTreeNode*>> &result,vector<BinaryTreeNode*> &tmp,int sum,int cur){
    cur += node->val;
    tmp.push_back(node);    
    if(node->left == NULL && node->right == NULL && cur == sum){
        result.push_back(tmp);
        tmp.pop_back();
        return;
    }
    if(node->left != NULL)
        helper(node->left,result,tmp,sum,cur);
    if(node->right != NULL){
        helper(node->right,result,tmp,sum,cur);
    }
    tmp.pop_back(); 
}
int pathSum(BinaryTreeNode* node,int sum,vector<vector<BinaryTreeNode*>> &result){
    if(node == NULL)return 0;
    std::vector<BinaryTreeNode*> tmp;
    helper(node,result,tmp,sum,0);
    return result.size();
}

