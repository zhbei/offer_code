void Test(const char* testName, BinaryTreeNode* pRoot, int expectedSum)
{
    if(testName != nullptr)
        printf("%s begins:\n", testName);

    vector<vector<BinaryTreeNode*>> result;
    cout << pathSum(pRoot,expectedSum,result ) << endl;

    printf("\n");
}

